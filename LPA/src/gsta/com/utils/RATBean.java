package gsta.com.utils;

public class RATBean {
	private String pprId;
	private String allowedOperator;
	private String endUserConsent;
	public String getPprId() {
		return pprId;
	}
	public void setPprId(String pprId) {
		this.pprId = pprId;
	}
	public String getAllowedOperator() {
		return allowedOperator;
	}
	public void setAllowedOperator(String allowedOperator) {
		this.allowedOperator = allowedOperator;
	}
	public String getEndUserConsent() {
		return endUserConsent;
	}
	public void setEndUserConsent(String endUserConsent) {
		this.endUserConsent = endUserConsent;
	}

}

package com.whty.euicc.common.caputils;

public class CapException extends Exception {

	private static final long serialVersionUID = -3721026046584056208L;

	public CapException() {
		super();
	}

	public CapException(String message, Throwable cause) {
		super(message, cause);
	}

	public CapException(String message) {
		super(message);
	}

	public CapException(Throwable cause) {
		super(cause);
	}
	
}
